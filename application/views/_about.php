<h2>What is Cheep?</h2>
<p>Cheep is a private, group communication tool.</p>
<p>We believe there aren't great places online to have private group discussions and we want to change that.</p>
<p></p>

<h2>How do I use Cheep?</h2>
<p>Simple!</p>
<ul>
	<li>Sign up.</li>
	<li>Create a new group.</li>
	<li>You'll be given an invite code to share with those people you want to include in the conversation.</li>
	<li>Groups are limited to 8 people currently.  Once the group has 8 people in it, the invitation code won't work.  If the group drops below 8, the invitation code will resume allowing people in until 8 is once again reached.</p>
	<li>They will need to join Cheep and enter in the invite code to be included in the conversation.</li>
	<li>The last person to leave the group "turns off the lights."  We delete the data in the group which is about as private as we can make it on our end.</li>
</ul>

<h2>How do I invite people to join me?</h2>
<p>Share the invite code.  That's it.  They'll need to sign up, of course, but you can share it however you desire.  And since it isn't tied to their email address, they can register whatever email they want with the site.  Everyone wins!</p>

<h2>I've been given an invite code and I don't know what to do</h2>
<p>You'll need to join Cheep and then enter the invite code.  This will put you right into the group your friend has invited you to!</p>

