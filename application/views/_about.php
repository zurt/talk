<h2>What is Cheep?</h2>
<p>Cheep is a private, group communication tool.</p>
<p>We believe there aren't great places online to have private group discussions and we want to change that.</p>
<p></p>

<h2>How do I use Cheep?</h2>
<p>Simple!</p>
<ul>
	<li>Sign up.</li>
	<li>Create a new group.</li>
	<li>You'll be given an invite code to share with those people you want to include in the conversation.</li>
	<li>Groups are limited to 8 people currently.  Once the group has 8 people in it, the invitation code won't work.  If the group drops below 8, the invitation code will resume allowing people in until 8 is once again reached.</p>
	<li>They will need to join Cheep and enter in the invite code to be included in the conversation.</li>
	<li>The last person to leave the group "turns off the lights."  We delete the data in the group which is about as private as we can make it on our end.</li>
</ul>

<h2>How do I invite people to join me?</h2>
<p>Share the invite code.  That's it.  They'll need to sign up, of course, but you can share it however you desire.  Because you share the code with them (instead of us sending out invitations on your behalf), they can register whatever email they want with the site.  Everyone wins!</p>
<p>Do note that everyone in the group has the invite code and can invite others to join.</p>

<h2>I've been given an invite code and I don't know what to do</h2>
<p>You'll need to join Cheep and then enter the invite code.  This will put you right into the group your friend has invited you to!</p>

<h2>I don't want to get email notifications</h2>
<p>No problem.  We want to let you know when something happens in your groups, but we understand if you'd prefer not to get email notifications.</p>
<p>Simply click the gear image in the top right (or follow <a href="/user/">this link</a>) and uncheck the box that says 'Receive email notifications'.

<h2>I want to insert a picture</h2>
<p>Right now, we support 4 HTML tags: img, b (or strong) and i.  If you want to include a picture, you'll need the URL for it.  Type in &lt;img src="[URL goes here, minus the brackets]"&gt; in your post and the image should appear.</p>

<h2>How do I get a profile picture?</h2>
<p>Go to <a href="http://gravatar.com">gravatar.com</a> and register your email with them and upload a photo.  The good news is that there are many sites on the Internet that use Gravatar, so doing this once will could give you a nice icon on many sites (including Wordpress and others).</p>

