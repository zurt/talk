<h2>What is Jabberlap?</h2>
<p>Jabberlap is a private, group communication tool.</p>
<p>We believe there aren't great places online to have private group discussions and we want to change that.</p>
<p></p>

<h2>How do I use Jabberlap?</h2>
<p>Simple!</p>
<ul>
	<li>Sign up.</li>
	<li>Create a new group.</li>
	<li>You'll be given an invite code to share with those people you want to include in the conversation.</li>
	<li>Groups are limited to 8 people currently.  Once the group has 8 people in it, the invitation code won't work.  If the group drops below 8, the invitation code will resume allowing people in until 8 is once again reached.</p>
	<li>They will need to join Jabberlap and enter in the invite code to be included in the conversation.</li>
	<li>The last person to leave the group "turns off the lights."  We delete the data in the group which is about as private as we can make it on our end.</li>
</ul>

<h2>How do I invite people to join me?</h2>
<p>There are two ways:</p>
<ol>
<li><p>Share the invite code.  That's it.  They'll need to sign up, of course, but you can share it however you desire.  Because you share the code with them (instead of us sending out invitations on your behalf), they can register whatever email they want with the site.  Everyone wins!</p>
<p>Do note that everyone in the group has the invite code and can invite others to join.</p></li>
<li><p>Even easier: have them send an email to the group.  The email address is shown on the main page, right next to the invite code.  By sending email, they will be joined to the site (and will get an email with an assigned password that they can change) and joined to the group -- and their email will be posted to the group!  So easy!</p></li>
</ol>

<h2>I've been given an invite code and I don't know what to do</h2>
<p>You'll need to join Jabberlap and then enter the invite code.  This will put you right into the group your friend has invited you to!</p>

<h2>I don't want to get email notifications</h2>
<p>No problem.  We want to let you know when something happens in your groups, but we understand if you'd prefer not to get email notifications.</p>
<p>Simply click the gear image in the top right (or follow <a href="/user/">this link</a>) and uncheck the box that says 'Receive email notifications'.</p>

<h2>What's this about email posting to the group?</h2>
<p>If you get an email notification, you can simply reply to the message to have your response automagically posted to the group.  Even better, if you include image attachments, we handle those too (up to 25 megs per message)!</p>
<p>We only support image attachments at the moment, but <a href="info@jabberlap.com">let us know</a> if there are other file types you'd like to be sharing.</p>
<p>Note: we will do the best we can at parsing your message, but email can be funky.  Apologies if we mangle it somehow.</p>

<h2>I want to insert a picture</h2>
<p>We use a modified form of what's known as bbCode.  This is the same markup you might have used on discussion forums all over the web.  Instead of putting in HTML, you simply use brackets and we do the rest!.  Here's the list of what we currently support:</p>
<ul>
	<li>bold: [b]your text[/b]</li>
	<li>italics: [i]your text[/i]</li>
	<li>images: [img]the image URL[/img]</li>
	<li>you tube videos: [yt]the you tube link[/yt]</li>
</ul>

<h2>How do I get a profile picture?</h2>
<p>Go to <a href="http://gravatar.com">gravatar.com</a> and register your email with them and upload a photo.  The good news is that there are many sites on the Internet that use Gravatar, so doing this once will could give you a nice icon on many sites (including Wordpress and others).</p>

